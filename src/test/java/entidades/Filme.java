package entidades;

public class Filme {

	int quantidadeFilme, valorAluguel;
	
	public void setQuantidadeFilme(int arg1) {
		this.quantidadeFilme = arg1;
	}

	public void setValorAluguel(int arg1) {
		this.valorAluguel = arg1;
	}

	public int getQuantidadeFilme() {
		return this.quantidadeFilme;
	}
	
	public int getValorAluguel() {
		return this.valorAluguel;
	}
	
}